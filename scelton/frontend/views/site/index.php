                <h2>Bidang-bidang Akuntansi</h2>
              
                <h3>Akuntansi Keuangan</h3>
                Akuntansi keuangan berfokus pada pelaporan informasi keuangan organisasi kepada pengguna informasi eksternal, seperti investor, calon investor dan kreditor. Juga untuk menghitung dan mencatat transaksi bisnis dan menyiapkan laporan keuangan untuk pengguna eksternal sesuai dengan standar akuntansi yang berlaku di Indonesia

                <h3>Akuntansi Manajemen</h3>
                Bidang ini berfokus pada pengukuran, analisis, dan pelaporan informasi yang dapat membantu manajer dalam membuat keputusan untuk memenuhi tujuan organisasi. Dalam akuntansi manajemen, tindakan dan laporan internal didasarkan pada analisis biaya-manfaat, dan tidak diharuskan untuk mengikuti standar akuntansi di Indonesia.

                Untuk penjelasan lengkap tentang akuntansi manajemen Anda dapat membaca artikel ini.

                <h3>Audit</h3>
                Audit adalah verifikasi asersi yang dibuat oleh orang lain mengenai suatu laporan keuangan. Dalam konteks akuntansi, audit juga berarti “akuntansi pemeriksaan” yang tidak memihak dan evaluasi laporan keuangan dalam suatu organisasi yang berupa layanan profesional yang sistematis dan konvensional. Untuk penjelasan lengkap mengenai audit, Anda bisa membacanya disini.

                <h3>Sistem Informasi Akuntansi</h3>
                Bidang akuntansi ini adalah bagian dari sistem informasi organisasi yang berfokus pada pemrosesan data keuangan. Banyak perusahaan menggunakan sistem informasi berbasis kecerdasan buatan. Industri perbankan dan keuangan menggunakan AI sebagai pendeteksi penipuan.

                Industri ritel menggunakan AI untuk layanan pelanggan. AI juga digunakan dalam industri keamanan siber.  Penjelasan lengkapnya dapat Anda melalui artikel ini.

                <h3>Akuntansi Pajak</h3>
                Untuk akuntansi perpajakan adalah struktur metode  yang berfokus pada pajak daripada tampilan laporan keuangan. Standar perpajakan juga berbeda dengan standar akuntansi keuangan atau PSAK, hal ini dikarenakan dalam perpajakan tidak ada namanya akuntansi, yang ada hanya pencatatan pembukuan saja. Untuk penjelesan lengkap tentang ini, Anda bisa membacanya melalui artikel ini.

                <h3>Akuntansi Forensik</h3>
                Bidang ini adalah perpaduan antara bidang ilmu akuntansi dengan kemampuan investigatif untuk memecahkan suatu masalah/sengketa keuangan atau dugaan fraud yang pada akhirnya akan diputuskan oleh pengadilan/ arbitrase/ tempat penyelesaian perkara lainnya.
              

