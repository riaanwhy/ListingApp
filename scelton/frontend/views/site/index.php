                <h2>Dasar-dasar Akuntansi</h2>

                <p>Dalam mempelajari akuntansi, Anda harus mengerti tentang istilah ALOE. 
                   Berikut adalah pengertian dari “A-L-O-E”.
                    <p>
                    A – Assets (Aset)
                    </p>
                    <p>
                    L – Liabilities (Kewajiban)
                    </p>
                    <p>
                    O E- Owner’s Equity (Ekuitas Pemilik)
                    </p>
                 </p>
                <p>
                    Ini adalah salah satu konsep dasar dalam akuntansi. Persamaan dasar akuntansi juga berlaku seperti ini:
                </p>
                <p>
                      Aset = Kewajiban + Ekuitas Pemilik
                </p>
                <p>

                    <h3>Assets</h3> Aset adalah barang-barang milik Anda dan Anda adalah pemiliknya. Barang-barang ini sesuai dengan “nilai” dan dapat memberi Anda uang sebagai imbalannya. Contoh Aset adalah Mobil, Rumah, dll.

                    <h3>Liabilities</h3> Apa pun yang Anda miliki adalah kewajiban. Bahkan pinjaman yang Anda ambil dari bank untuk membeli segala jenis aset adalah kewajiban.

                    <h3>Owner’s Equity</h3> Jumlah total uang tunai yang diinvestasikan seseorang dalam suatu organisasi adalah Ekuitas Pemilik. Investasi yang dilakukan tidak selalu berupa uang, bisa saja dalam bentuk stok produk juga.
                </p>

